            var $el = e.data.el;
            $this = $(this),
                $next = $this.next();

            $next.slideToggle();
            $this.parent().toggleClass('isOpen');

            if (!e.data.multiple) {
                $el.find('.faq-content').not($next).slideUp().parent().removeClass('isOpen');
            };
        }

